menu=["chicken","bun","rice","kottu"]

food=input("Enter key ? ")

#in condition
if food in menu:
    print(food)


# if condition
if food=="bun":
    print(food)

# elif condition
if food=="bun":
    print(food)
elif food=="rice":
    print(food)


# else condition
if food=="bun":
    print(food)
else :
    print(food)
